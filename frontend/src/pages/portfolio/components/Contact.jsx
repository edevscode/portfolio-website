const EMPTY = { name: '', email: '', subject: '', message: '' }

export default function Contact({ about, socialLinks }) {
  const { config: seasonConfig } = useSeasonContext()
  const { theme } = useTheme()

  const primary = theme?.primary_color || seasonConfig?.colors?.primary || '#6366f1'
  const accent  = theme?.accent_color  || seasonConfig?.colors?.accent  || '#4da6ff'
  const bg      = theme?.background_color || seasonConfig?.colors?.background || '#fff'
  const text    = theme?.text_color || seasonConfig?.colors?.text || '#111'

  const [formData, setFormData] = useState(EMPTY)
  const [status, setStatus] = useState(null) // null | 'sending' | 'success' | 'error'

  const handleChange = (e) => setFormData(prev => ({ ...prev, [e.target.name]: e.target.value }))

  const handleSubmit = async (e) => {
    e.preventDefault()
    setStatus('sending')
    try {
      await apiService.createContact(formData)
      setStatus('success')
      setFormData(EMPTY)
    } catch {
      setStatus('error')
    }
  }

  const visibleLinks = (socialLinks || []).filter(l => l.is_visible !== false)

  return (
    <section
      className="contact-section"
      id="contact"
      style={{ '--cp': primary, '--ca': accent, '--cb': bg, '--ct': text }}
    >
      <div className="contact-inner">

        {/* Heading */}
        <div className="contact-heading">
          <span className="contact-label">Contact</span>
          <h2 className="contact-title">Let's Work Together</h2>
          <p className="contact-subtitle">
            Have a project in mind or just want to say hello? I'd love to hear from you.
          </p>
        </div>

        <div className="contact-body">

          {/* Left: info panel */}
          <div className="contact-info">
            {about?.email && (
              <a href={`mailto:${about.email}`} className="contact-info-row">
                <span className="contact-info-icon">
                  <svg width="18" height="18" viewBox="0 0 24 24" fill="none" stroke="currentColor" strokeWidth="1.8" strokeLinecap="round" strokeLinejoin="round">
                    <rect x="2" y="4" width="20" height="16" rx="2"/>
                    <path d="m22 7-8.97 5.7a1.94 1.94 0 01-2.06 0L2 7"/>
                  </svg>
                </span>
                <span className="contact-info-text">{about.email}</span>
              </a>
            )}
            {about?.phone && (
              <a href={`tel:${about.phone}`} className="contact-info-row">
                <span className="contact-info-icon">
                  <svg width="18" height="18" viewBox="0 0 24 24" fill="none" stroke="currentColor" strokeWidth="1.8" strokeLinecap="round" strokeLinejoin="round">
                    <path d="M22 16.92v3a2 2 0 01-2.18 2 19.79 19.79 0 01-8.63-3.07A19.5 19.5 0 013.07 10.8a19.79 19.79 0 01-3.07-8.72A2 2 0 012 0h3a2 2 0 012 1.72c.127.96.361 1.903.7 2.81a2 2 0 01-.45 2.11L6.91 7.91a16 16 0 006.18 6.18l1.27-.83a2 2 0 012.11-.45c.907.339 1.85.573 2.81.7A2 2 0 0122 16.92z"/>
                  </svg>
                </span>
                <span className="contact-info-text">{about.phone}</span>
              </a>
            )}
            {about?.location && (
              <div className="contact-info-row contact-info-row--static">
                <span className="contact-info-icon">
                  <svg width="18" height="18" viewBox="0 0 24 24" fill="none" stroke="currentColor" strokeWidth="1.8" strokeLinecap="round" strokeLinejoin="round">
                    <path d="M21 10c0 7-9 13-9 13s-9-6-9-13a9 9 0 0118 0z"/>
                    <circle cx="12" cy="10" r="3"/>
                  </svg>
                </span>
                <span className="contact-info-text">{about.location}</span>
              </div>
            )}

            {visibleLinks.length > 0 && (
              <div className="contact-social">
                <p className="contact-social-label">Find me on</p>
                <div className="contact-social-links">
                  {visibleLinks.map(link => (
                    <a
                      key={link.id}
                      href={link.url}
                      target="_blank"
                      rel="noopener noreferrer"
                      className="contact-social-btn"
                      title={link.platform}
                      aria-label={link.platform}
                    >
                      <PlatformIcon platform={link.platform} size={18} />
                      <span>{link.platform}</span>
                    </a>
                  ))}
                </div>
              </div>
            )}
          </div>

          {/* Right: form */}
          <div className="contact-form-wrap">
            {status === 'success' ? (
              <div className="contact-success">
                <div className="contact-success-icon">
                  <svg width="32" height="32" viewBox="0 0 24 24" fill="none" stroke="currentColor" strokeWidth="2" strokeLinecap="round" strokeLinejoin="round">
                    <path d="M22 11.08V12a10 10 0 11-5.93-9.14"/>
                    <polyline points="22 4 12 14.01 9 11.01"/>
                  </svg>
                </div>
                <h3>Message sent!</h3>
                <p>Thanks for reaching out. I'll get back to you soon.</p>
                <button className="contact-reset-btn" onClick={() => setStatus(null)}>
                  Send another message
                </button>
              </div>
            ) : (
              <form className="contact-form" onSubmit={handleSubmit} noValidate>
                <div className="contact-form-row">
                  <div className="contact-field">
                    <label htmlFor="cf-name">Name</label>
                    <input
                      id="cf-name"
                      type="text"
                      name="name"
                      value={formData.name}
                      onChange={handleChange}
                      placeholder="Your name"
                      required
                    />
                  </div>
                  <div className="contact-field">
                    <label htmlFor="cf-email">Email</label>
                    <input
                      id="cf-email"
                      type="email"
                      name="email"
                      value={formData.email}
                      onChange={handleChange}
                      placeholder="your@email.com"
                      required
                    />
                  </div>
                </div>
                <div className="contact-field">
                  <label htmlFor="cf-subject">Subject</label>
                  <input
                    id="cf-subject"
                    type="text"
                    name="subject"
                    value={formData.subject}
                    onChange={handleChange}
                    placeholder="What's this about?"
                    required
                  />
                </div>
                <div className="contact-field">
                  <label htmlFor="cf-message">Message</label>
                  <textarea
                    id="cf-message"
                    name="message"
                    value={formData.message}
                    onChange={handleChange}
                    placeholder="Tell me about your project or question…"
                    rows={5}
                    required
                  />
                </div>

                {status === 'error' && (
                  <p className="contact-error">Something went wrong. Please try again.</p>
                )}

                <button
                  type="submit"
                  className="contact-submit"
                  disabled={status === 'sending'}
                >
                  {status === 'sending' ? (
                    <><span className="contact-spinner" /> Sending…</>
                  ) : (
                    <>
                      Send Message
                      <svg width="16" height="16" viewBox="0 0 24 24" fill="none" stroke="currentColor" strokeWidth="2" strokeLinecap="round" strokeLinejoin="round">
                        <line x1="22" y1="2" x2="11" y2="13"/>
                        <polygon points="22 2 15 22 11 13 2 9 22 2"/>
                      </svg>
                    </>
                  )}
                </button>
              </form>
            )}
          </div>

        </div>
      </div>
    </section>
  )
}
